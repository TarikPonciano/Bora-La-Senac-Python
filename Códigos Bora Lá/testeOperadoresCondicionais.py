num1 = int(input("Insira o número 1: "))
num2 = int(input("Insira o número 2: "))

igualdade = num1 == num2

desigualdade = num1 != num2

maior = num1 > num2

menor = num1 < num2

maiorIgual = num1 >= num2

menorIgual = num1 <= num2

print("Igualdade:", igualdade)
print("Desigualdade:",desigualdade)
print("Maior:", maior)
print("Menor:", menor)
print("Maior ou Igual:", maiorIgual)
print("Menor ou Igual:", menorIgual)